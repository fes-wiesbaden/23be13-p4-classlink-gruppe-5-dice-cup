CREATE TABLE audit_logs
(
    id        BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    actor_id  UUID                                    NOT NULL,
    action    VARCHAR(255)                            NOT NULL,
    timestamp TIMESTAMP WITHOUT TIME ZONE             NOT NULL,
    details   VARCHAR(255)                            NOT NULL,
    CONSTRAINT pk_audit_logs PRIMARY KEY (id)
);

CREATE TABLE class
(
    id   UUID         NOT NULL,
    name VARCHAR(100) NOT NULL,
    CONSTRAINT pk_class PRIMARY KEY (id)
);

CREATE TABLE class_projects
(
    id         UUID    NOT NULL,
    class_id   UUID    NOT NULL,
    project_id UUID    NOT NULL,
    active     BOOLEAN NOT NULL,
    valid_from TIMESTAMP WITHOUT TIME ZONE,
    valid_to   TIMESTAMP WITHOUT TIME ZONE,
    CONSTRAINT pk_class_projects PRIMARY KEY (id)
);

CREATE TABLE grades
(
    id                 UUID                        NOT NULL,
    project_subject_id BIGINT                      NOT NULL,
    student_id         UUID                        NOT NULL,
    teacher_id         UUID                        NOT NULL,
    grade_value        DOUBLE PRECISION            NOT NULL,
    created_at         TIMESTAMP WITHOUT TIME ZONE NOT NULL,
    CONSTRAINT pk_grades PRIMARY KEY (id)
);

CREATE TABLE group_members
(
    id         UUID NOT NULL,
    student_id UUID NOT NULL,
    group_id   UUID NOT NULL,
    CONSTRAINT pk_group_members PRIMARY KEY (id)
);

CREATE TABLE project
(
    id          UUID                        NOT NULL,
    class_id    UUID,
    name        VARCHAR(200),
    description VARCHAR(255),
    created_by  UUID,
    created_at  TIMESTAMP WITHOUT TIME ZONE NOT NULL,
    active      BOOLEAN                     NOT NULL,
    CONSTRAINT pk_project PRIMARY KEY (id)
);

CREATE TABLE project_subject
(
    id         BIGINT        NOT NULL,
    project_id UUID          NOT NULL,
    subject_id UUID          NOT NULL,
    weight     DECIMAL(5, 2) NOT NULL,
    CONSTRAINT pk_project_subject PRIMARY KEY (id)
);

CREATE TABLE registration_invites
(
    id         UUID                        NOT NULL,
    email      VARCHAR(255)                NOT NULL,
    role       VARCHAR(255)                NOT NULL,
    token_hash VARCHAR(255)                NOT NULL,
    expires_at TIMESTAMP WITHOUT TIME ZONE NOT NULL,
    created_at TIMESTAMP WITHOUT TIME ZONE NOT NULL,
    created_by UUID,
    used_at    TIMESTAMP WITHOUT TIME ZONE,
    CONSTRAINT pk_registration_invites PRIMARY KEY (id)
);

CREATE TABLE student_groups
(
    id           UUID    NOT NULL,
    project_id   UUID    NOT NULL,
    group_number INTEGER NOT NULL,
    CONSTRAINT pk_student_groups PRIMARY KEY (id)
);

CREATE TABLE students
(
    id UUID NOT NULL,
    CONSTRAINT pk_students PRIMARY KEY (id)
);

CREATE TABLE subject
(
    id          UUID NOT NULL,
    name        VARCHAR(100),
    description VARCHAR(255),
    CONSTRAINT pk_subject PRIMARY KEY (id)
);

CREATE TABLE teacher
(
    user_id UUID NOT NULL,
    CONSTRAINT pk_teacher PRIMARY KEY (user_id)
);

CREATE TABLE user_info
(
    user_id       UUID NOT NULL,
    first_name    VARCHAR(100),
    last_name     VARCHAR(100),
    date_of_birth date,
    email         VARCHAR(255),
    CONSTRAINT pk_userinfo PRIMARY KEY (user_id)
);

CREATE TABLE users
(
    id            UUID                        NOT NULL,
    username      VARCHAR(100)                NOT NULL,
    password_hash VARCHAR(255)                NOT NULL,
    enabled       BOOLEAN                     NOT NULL,
    disabled_at   TIMESTAMP WITHOUT TIME ZONE,
    created_at    TIMESTAMP WITHOUT TIME ZONE NOT NULL,
    created_by    UUID                        NOT NULL,
    updated_at    TIMESTAMP WITHOUT TIME ZONE,
    updated_by    UUID,
    version       BIGINT                      NOT NULL,
    CONSTRAINT pk_users PRIMARY KEY (id)
);

